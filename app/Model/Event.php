	function isInCamp($field=array())
	{
		$siteId = $field['site_id'];
		$camp = $this->Camp->findById($this->data[$this->name]['camp_id']);
		foreach($camp['Site'] as $site)
		{
			if($site['id'] == $siteId)
				return true;
		}
		unset($site);
		return false;
	}

